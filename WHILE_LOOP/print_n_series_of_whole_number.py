n = int(input("Enter series of number: ")) # Taking inout from user
i = 0 # Initialising a looping variable
while i <= n: # if i will be less than or equals to then this loop will run
    print(i) # Printing i "n" number of times till loop runs
    i += 1 # Updation of looping variable. If it is not done then it will stuck in infinite loop