# WAP_to_print_the_series_of_n_square_of_n_natural_numbers
n = int(input("Enter number of series till where you want the square of number: ")) # Taking input from user
i = 1 # Initialising a looping variable
while i <= n: # if i is less than or equals to n then the loop runs
    print(i) # Printing i till loop runs
    i += 1 # Updation of looping variable. If it is not done then it will stuck in infinite loop
